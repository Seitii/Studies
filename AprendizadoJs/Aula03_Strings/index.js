let umaString = "Um texto";
console.log(umaString.length); // Retorna o tamanho da string   
console.log(umaString[4]); // Retorna o caracter na posição 4
console.log(umaString.slice(3, 8)); // Retorna o caracter na posição 3 até a posição 8  
console.log(umaString.toLocaleLowerCase()); // Retorna a string em caixa baixa
console.log(umaString.toLocaleUpperCase()); // Retorna a string em caixa alta

