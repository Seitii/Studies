# TIPOS DE PRINTS 
print("Olá mundo!")   # STRING
print(10)  # INT
print(10.5) # FLOAT
print(10 == 10) # EQUALS == verdadeiro 
print(10 == 11) # EQUALS == falso
print(type("Olá mundo!")) # TYPE STRING
print(type(10)) # TYPE INT
print(type(10.5)) # TYPE FLOAT 
print(type(True)) # TYPE BOOLEAN 
print(type(False)) # TYPE BOOLEAN 
print(type(10== 10)) # TYPE BOOLEAN 
print(type(10 == 11)) # TYPE BOOLEAN 
print(int('1', type(int('1')))) # converte string para inteiro
print(type(float('1') + 1 )) # converte string para float
print(bool('')) # converte string para boolean
print(str(11) + 'b') # converte inteiro para string e adiciona 'b'

